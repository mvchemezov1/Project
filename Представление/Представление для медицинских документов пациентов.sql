CREATE VIEW V_MedicalDocuments AS
SELECT 
    Д.ID AS Документ_ID,
    П.Фамилия AS Фамилия_Пациента,
    П.Имя AS Имя_Пациента,
    Д.Тип_документа AS Тип_документа,
    Д.Дата_выдачи AS Дата_выдачи,
    В.Фамилия AS Фамилия_Врача,
    В.Имя AS Имя_Врача
FROM 
    Медицинские_документы Д
JOIN 
    Пациент П ON Д.Пациент = П.ID
JOIN 
    Врач В ON Д.Врач = В.ID;
